//To multiply number without actualy multiplying the numbers.//
#include<iostream>
using namespace std;
int main()
{
    int a,b,sum,z;
    cout<<"Enter two numbers: ";
    cin>>a>>b;
    while (b!=0)
    {
        sum+=a;
        --b;
        z=9/2+8*5;
        cout<<z;
    }
    cout<<"Answer is "<<sum<<endl;
    return 0;
}